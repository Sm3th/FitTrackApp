export const getExerciseGuide = (exerciseId: string): ExerciseGuide | undefined => {
  return EXERCISE_GUIDES[exerciseId];
};

export const getExerciseGuideByName = (name: string): ExerciseGuide | undefined => {
  const normalizedName = name.toLowerCase().replace(/[^a-z0-9]/g, '-').replace(/-+/g, '-');
  return EXERCISE_GUIDES[normalizedName] || Object.values(EXERCISE_GUIDES).find(
    g => g.name.toLowerCase() === name.toLowerCase()
  );
};
